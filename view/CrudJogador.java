    private void jbDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeletarActionPerformed
        int linha = jtJogador.getSelectedRow();
        String cpf = (String) jtJogador.getValueAt(linha, 0);
        JogadorServicos pessoaS = ServicosFactory.getJogadorServicos();
        String nome = pessoaS.getjogadorByDoc(cpf).getNome();
        Object[] btnMSG = {"Sim", "Não"};
        int resp = JOptionPane.showOptionDialog(this, "Deseja realmente deletar " + nome, "Deletar Jogador", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, btnMSG, btnMSG[0]);
        if (resp == 0) {
            pessoaS.deletarJogador(cpf);
            addRowToTable();
            JOptionPane.showMessageDialog(this, "Cadastro removido");
        } else {
            JOptionPane.showMessageDialog(this, "Operação cancelada pelo usuário");
        }
        jbLimpar.doClick();
        // TODO add your handling code here:
    }//GEN-LAST:event_jbDeletarActionPerformed

    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        jbDeletar.setVisible(false);
        jbSalvar.setText("Confirmar");
        jbLimpar.setText("Cancelar");
        //jtfCPF.setEnabled(false);
        int linha = jtJogador.getSelectedRow();
        String cpf = (String) jtJogador.getValueAt(linha, 0);
        JogadorServicos pessoaS = ServicosFactory.getJogadorServicos();
        Jogador p = pessoaS.getjogadorByDoc(cpf);
        jtfNome.setText(p.getNome());
        jtfTelefone.setText(p.getTelefone());
        jtfEmail.setText(p.getEmail());
        jtfCidade.setText(p.getCidade());        // TODO add your handling code here:
    }//GEN-LAST:event_jbEditarActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
if (validInputs()) {
            
            String nome = jtfNome.getText().toUpperCase();
            String telefone = jtfTelefone.getText().toUpperCase();
            String email = jtfEmail.getText();
            String cidade = jtfCidade.getText().toUpperCase();
            String cpf = jtfCpf.getText();
            JogadorServicos jogadorJ = ServicosFactory.getJogadorServicos();
            Jogador j = new Jogador(0, nome, telefone, email, cidade, cpf);
            if (jbSalvar.getText().equals("Salvar")) {
                jogadorJ.cadastroJogador(j);
            } else {
                jogadorJ.atualizarJogadorDAO(j);
            }
            addRowToTable();
            limparCampos();

        }        // TODO add your handling code here:
    }//GEN-LAST:event_jbSalvarActionPerformed

