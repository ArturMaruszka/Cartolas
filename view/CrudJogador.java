    public void addRowToTable() {
        DefaultTableModel model = (DefaultTableModel) jtJogador.getModel();
        model.getDataVector().removeAllElements();//remove todas as linhas
        model.fireTableDataChanged();
        Object rowData[] = new Object[4];
        JogadorServicos jogadorJ = ServicosFactory.getJogadorServicos();
        for (Jogador j : jogadorJ.getJogador()) {
            rowData[0] = j.getNome();
            rowData[1] = j.getTelefone();
            rowData[2] = j.getEmail();
            rowData[3] = j.getCidade();
            model.addRow(rowData);
        }
    }

    public boolean validInputs() {
        
        if (jtfNome.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo Nome obrigatório!");
            jtfNome.requestFocus();
            return false;
        } else if (jtfEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo Endereco obrigatório!");
            jtfEmail.requestFocus();
            return false;
        } else if (jtfTelefone.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo Telefone obrigatório!");
            jtfTelefone.requestFocus();
            return false;
        }
        return true;
    }

